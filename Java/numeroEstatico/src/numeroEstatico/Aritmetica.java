package numeroEstatico;

public class Aritmetica {
	static public int subtrair(int x, int y) {
		return x - y;
	};
	
	static public int somar(int x, int y) {
		return x + y;
	};
	
	static public int multiplicar(int x, int y) {
		return x * y;
	};
	
	static public int dividir(int x, int y) {
		return x / y;
	};
}
